def mysum(x, y): 
    """ 
    Takes two input arguments x and y and returns their sum 
    """ 
    s = x + y 
    return s 
def myproduct(x, y): 
    """ 
    Takes two input arguments x and y and returns their product 
    """ 
    s = x * y 
    return s 
def main(): 
    a = 5 
    b = 3 
    print(mysum(a, b))     
    print(myproduct(a, b))    
main() 
# hopefully outputs 8 
# hopefully outputs 15